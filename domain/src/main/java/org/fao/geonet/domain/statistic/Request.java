package org.fao.geonet.domain.statistic;

import java.util.ArrayList;
import java.util.List;

import javax.persistence.Access;
import javax.persistence.AccessType;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;

/**
 * An entity representing a request that was made by a user.  
 * This is part of the statistics tracing
 *
 * @author Jesse
 */
@Entity
@Access(AccessType.PROPERTY)
public class Request {
    private int _id;
    private String _requestDate;
    private String _ipAddress;
    private String _query;
    private int _hits;
    private String _lang;
    private String _sortBy;
    private String _spatialFilter;
    private String _type;
    private int _simple;
    private int _autogenerated;
    private String _service;
    private RequestParams _params;

    @Id
    public int getId() {
        return _id;
    }

    public void setId(int id) {
        this._id = id;
    }
    @OneToOne(cascade=CascadeType.ALL, orphanRemoval=true, fetch=FetchType.LAZY)
    @JoinColumn(name="requestid")
    public RequestParams getParams() {
        return _params;
    }
    public void setParams(RequestParams params) {
        this._params = params;
    }
    @Column(name = "requestdate")
    public String getRequestDate() {
        return _requestDate;
    }

    public void setRequestDate(String requestDate) {
        this._requestDate = requestDate;
    }

    @Column(name = "ip")
    public String getIpAddress() {
        return _ipAddress;
    }

    public void setIpAddress(String ipAddress) {
        this._ipAddress = ipAddress;
    }

    public String getQuery() {
        return _query;
    }

    public void setQuery(String query) {
        this._query = query;
    }

    public int getHits() {
        return _hits;
    }

    public void setHits(int hits) {
        this._hits = hits;
    }

    public String getLang() {
        return _lang;
    }

    public void setLang(String lang) {
        this._lang = lang;
    }

    @Column(name = "sortby")
    public String getSortBy() {
        return _sortBy;
    }

    public void setSortBy(String sortBy) {
        this._sortBy = sortBy;
    }

    @Column(name = "spatialfilter")
    public String getSpatialFilter() {
        return _spatialFilter;
    }

    public void setSpatialFilter(String spatialFilter) {
        this._spatialFilter = spatialFilter;
    }

    public String getType() {
        return _type;
    }

    public void setType(String type) {
        this._type = type;
    }

    public int getSimple() {
        return _simple;
    }

    public void setSimple(int simple) {
        this._simple = simple;
    }

    public int getAutogenerated() {
        return _autogenerated;
    }

    public void setAutogenerated(int autogenerated) {
        this._autogenerated = autogenerated;
    }

    public String getService() {
        return _service;
    }

    public void setService(String service) {
        this._service = service;
    }

}
